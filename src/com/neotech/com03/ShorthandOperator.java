package com.neotech.com03;

public class ShorthandOperator {

	public static void main(String[] args) {
		
	
	int num1  = 5;
	int num2 = 10; 
	
	
	// shorthanded by num1 += 
	num1 += 5;
	
	
	System.out.println(num1); 
	
	num2 *= 2;
	System.out.println(num2);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

	
	
	
	
	
}
